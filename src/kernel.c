inline u8 read_port(u16 port)
{
    u8 result = 0;
    __asm__ __volatile__("in %%dx, %%al" : "=a" (result) : "d" (port));
    return result;
}

inline void write_port(u16 port, u8 data)
{
    __asm__ __volatile__("out %%al, %%dx" : : "a" (data), "d" (port));
}


